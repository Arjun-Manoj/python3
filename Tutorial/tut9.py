'''Number Systems and their conversions in Python3'''
##Binary(0b), Decimal, Hexadecimal(0x) and Octal(0o) Systems of numbers

#Decimal to binary
dec_num  = 62
print(bin(dec_num))
print(type(dec_num))

#Binary to Decimal
bin_num = 0b111
print(bin_num)
print(type(bin_num))

#Binary to Octal
print(oct(0b111))
print(type(oct(0b111)))

#Octal to Binary
print(bin(0o456))
print(type(bin(0o456)))

#Binary to Hexadecimal
print(hex(0b1101))
print(type(hex(0b1101)))

#Hexadecimal to binary
print(bin(0x14f))
print(type(bin(0x14f)))

#Octal to Decimal
print(0o456)
print(type(0o456))

#Decimal to Octal
print(oct(302))
print(type(oct(302)))

#Octal to Hexadecimal
print(hex(0o456))
print(type(hex(0o456)))

#Hexadecimal to Octal
print(oct(0x14f))
print(type(oct(0x14f)))

#Decimal to Hexadecimal
print(hex(149))
print(type(hex(149)))

#Hexadecimal to Decimal
print(0x14f)
print(type(0x14f))