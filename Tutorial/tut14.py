'''if else elif'''

#Basic syntax of if statements
if True:
    print("This line appears in console!")
    
if False:
    print("This line does not appear in console!")
    
#Checking even and odd 

x = 3
y = 2

if x%2!=0:
    print("x is odd")
    
if y%2==0:
    print("y is even")
    
#Lets use if-else blocks 

z = 87
if z%2==0:
    print(z,"is even")
else:
    print(z,"is odd")
    
#Using elif statements

num = 3
if num == 1:
    print("One")
elif num == 2:
    print("Two")
elif num == 3:
    print("Three")
elif num == 4:
    print("Four")
    
