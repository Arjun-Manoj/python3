'''Bitwise Operators'''

#Compliment -- unary operator (2s compliment)
print(~12)


print("")
#Bitwise AND operator
print(12&13)
print(5&0)
print(2&7)
print(3&157)


print("")
#Bitwise OR operator
print(12|13)
print(5|0)
print(2|7)
print(3|157)


print("")
#Bitwise XOR operator
print(12^13)
print(5^0)
print(2^7)
print(3^157)


print("")

#Leftshift operator
print(10<<2)
print(16<<1)
print(12<<4)
print(7<<3)


print("")

#Rightshift operator
#Leftshift operator
print(10>>2)
print(16>>1)
print(12>>4)
print(7>>3)

print("")